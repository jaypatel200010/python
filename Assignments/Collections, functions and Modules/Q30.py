# 30]  Write a Python program to convert a list of tuples into a dictionary.
list1 = [("jay", 75), ("ketan", 111), ("uday", 21), ("raj", 75), ("tushar", 111), ("himanshu", 21)]
print("List of tuples = ", list1)
# Convert the list of tuples into a dictionary
convert_dict = dict(list1)
# Display the resulting dictionary after conversion
print("List of tuples converted into dictionary = ", convert_dict)