'''
64]  Write a Python program to find the maximum and minimum numbers from the specified
decimal numbers.
'''
# List of decimal numbers
numbers = [3.12,2.13,4.54,6.34,8.98]
# Find the maximum and minimum numbers
max_number = max(numbers)  # Find the maximum number using max()
min_number = min(numbers)  # Find the minimum number using min()
# Display the maximum and minimum numbers
print(f"The maximum number is: {max_number}")
print(f"The minimum number is: {min_number}")